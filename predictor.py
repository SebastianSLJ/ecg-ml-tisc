def cargar_csv_ecg(filepath):
    """Carga ECG desde un archivo CSV local"""
    try:
        df = pd.read_csv(filepath)
        
        # Estructura de clase simple para compatibilidad
        class RecordCSV:
            def __init__(self, data):
                self.p_signal = data['ecg_filtrado'].values.reshape(-1, 1)
                self.fs = 360.23  # Frecuencia de muestreo del CSV
                self.n_sig = 1
                self.sig_name = ['ECG']
        
        class AnnotationCSV:
            def __init__(self):
                self.sample = np.array([])
                self.symbol = np.array([])
        
        record = RecordCSV(df)
        annotation = AnnotationCSV()
        return record, annotation
    except Exception as e:
        print(f"Error cargando CSV {filepath}: {e}")
        return None, None

