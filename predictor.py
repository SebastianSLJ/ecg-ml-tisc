"""# Entrenamiento del modelo"""

def entrenar_modelo_clasificacion(latidos, etiquetas):

    print("ENTRENAMIENTO DEL MODELO")

    # Extraer características de todos los latidos
    print("\n Extrayendo características avanzadas...")
    caracteristicas_lista = []
    etiquetas_filtradas = []

    for i, latido in enumerate(latidos):
        try:
            carac = extraer_caracteristicas_avanzadas(latido)
            caracteristicas_lista.append(list(carac.values()))
            etiquetas_filtradas.append(etiquetas[i])
        except Exception as e:
            continue

        if (i + 1) % 500 == 0:
            print(f"   Procesados: {i+1}/{len(latidos)} latidos...")

    X = np.array(caracteristicas_lista)
    y = np.array(etiquetas_filtradas)

    print(f"\n Características extraídas: {X.shape[1]} features por latido")

    # Filtrar clases con pocas muestras y mapear clases
    contador = Counter(y)
    print(f"\n Distribución original de clases:")
    for clase, count in sorted(contador.items(), key=lambda x: x[1], reverse=True):
        print(f" {clase}: {count} muestras ({count/len(y)*100:.1f}%)")

    # Mantener clases con más de 50 muestras para mejor generalización
    clases_validas = [clase for clase, count in contador.items() if count > 50]
    mascara = np.isin(y, clases_validas)
    X = X[mascara]
    y = y[mascara]

    print(f"\n Clases seleccionadas (>50 muestras): {clases_validas}")
    print(f" Total de muestras después de filtrado: {len(X)}")

    # Balanceo de clases con SMOTE (si está disponible)
    if SMOTE_AVAILABLE and len(clases_validas) > 1:
        print("\n  Aplicando SMOTE para balancear clases...")
        try:
            smote = SMOTE(random_state=42, k_neighbors=min(5, min(Counter(y).values())-1))
            X, y = smote.fit_resample(X, y)
            print(f" Dataset balanceado. Nuevas dimensiones: {X.shape}")

            contador_balanceado = Counter(y)
            print(f"\n Distribución después de SMOTE:")
            for clase, count in sorted(contador_balanceado.items()):
                print(f"   {clase}: {count} muestras")
        except Exception as e:
            print(f"  No se pudo aplicar SMOTE: {e}")

    # Dividir datos
    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=0.3, random_state=42, stratify=y
    )

    print(f"\n División de datos:")
    print(f"   Entrenamiento: {len(X_train)} muestras")
    print(f"   Prueba: {len(X_test)} muestras")

    # Estandarizar características
    scaler = StandardScaler()
    X_train_scaled = scaler.fit_transform(X_train)
    X_test_scaled = scaler.transform(X_test)

    # Entrenar modelo
    print("\n Entrenando Random Forest...")
    modelo = RandomForestClassifier(
        n_estimators=200,  # Más árboles para mejor rendimiento
        max_depth=20,
        min_samples_split=5,
        min_samples_leaf=2,
        random_state=42,
        n_jobs=-1  # Usar todos los cores
    )
    modelo.fit(X_train_scaled, y_train)
    print(" Modelo entrenado exitosamente")

    # Validación cruzada
    print("\n Realizando validación cruzada (5-fold)...")
    cv_scores = cross_val_score(modelo, X_train_scaled, y_train, cv=5, n_jobs=-1)
    print(f"   Precisión CV: {cv_scores.mean():.4f} (+/- {cv_scores.std():.4f})")
    print(f"   Scores individuales: {[f'{s:.4f}' for s in cv_scores]}")

    # Evaluar modelo
    y_pred = modelo.predict(X_test_scaled)
    accuracy = accuracy_score(y_test, y_pred)

    print("\n" + "="*70)
    print(" RESULTADOS DEL MODELO")
    print("="*70)
    print(f"\n Precisión en Test: {accuracy:.4f}")
    print("\n Reporte de Clasificación:")
    print(classification_report(y_test, y_pred))

    # Matriz de confusión
    plt.figure(figsize=(12, 10))
    cm = confusion_matrix(y_test, y_pred)

    # Normalizar por filas para ver porcentajes
    cm_norm = cm.astype('float') / cm.sum(axis=1)[:, np.newaxis]

    # Crear anotaciones que muestren conteo y porcentaje
    annotations = np.empty_like(cm).astype(str)
    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            annotations[i, j] = f'{cm[i,j]}\n({cm_norm[i,j]*100:.1f}%)'

    sns.heatmap(cm_norm, annot=annotations, fmt='', cmap='Blues',
                xticklabels=modelo.classes_,
                yticklabels=modelo.classes_,
                cbar_kws={'label': 'Proporción'},
                vmin=0, vmax=1)
    plt.title('Matriz de Confusión - Clasificación de Arritmias',
              fontsize=14, fontweight='bold')
    plt.xlabel('Predicho', fontsize=12)
    plt.ylabel('Real', fontsize=12)
    plt.tight_layout()
    plt.show()

    # Importancia de características
    feature_names = list(extraer_caracteristicas_avanzadas(latidos[0]).keys())
    importances = modelo.feature_importances_
    indices = np.argsort(importances)[::-1][:15]  # Top 15

    plt.figure(figsize=(12, 6))
    plt.title('Top 15 Características Más Importantes', fontsize=14, fontweight='bold')
    plt.bar(range(15), importances[indices], color='steelblue', alpha=0.7)
    plt.xticks(range(15), [feature_names[i] for i in indices], rotation=45, ha='right')
    plt.ylabel('Importancia')
    plt.xlabel('Característica')
    plt.tight_layout()
    plt.show()

    return modelo, scaler, feature_names

