"""# Preprocesamiento de señales"""

def preprocesar_ecg(senal, fs=360):
    """Aplica preprocesamiento a la señal ECG"""
    # Remover componente DC
    senal = senal - np.mean(senal)

    # Filtro pasa banda (0.5 - 40 Hz) para eliminar ruido
    nyquist = fs / 2
    low = 0.5 / nyquist
    high = 40.0 / nyquist
    b, a = signal.butter(3, [low, high], btype='band')
    senal_filtrada = signal.filtfilt(b, a, senal)

    return senal_filtrada

def extraer_caracteristicas_avanzadas(senal, fs=360):
    """Extrae características avanzadas de la señal ECG"""
    caracteristicas = {}

    #  Características estadísticas básicas
    caracteristicas['media'] = np.mean(senal)
    caracteristicas['std'] = np.std(senal)
    caracteristicas['rms'] = np.sqrt(np.mean(senal**2))
    caracteristicas['max'] = np.max(senal)
    caracteristicas['min'] = np.min(senal)
    caracteristicas['rango'] = np.max(senal) - np.min(senal)
    caracteristicas['skewness'] = pd.Series(senal).skew()
    caracteristicas['kurtosis'] = pd.Series(senal).kurtosis()

    #  Características espectrales
    try:
        f, Pxx = signal.welch(senal, fs, nperseg=min(256, len(senal)))
        caracteristicas['frecuencia_dominante'] = f[np.argmax(Pxx)]
        caracteristicas['potencia_espectral'] = np.sum(Pxx)
        caracteristicas['entropia_espectral'] = -np.sum(Pxx * np.log2(Pxx + 1e-10))
    except Exception as e:
        caracteristicas['frecuencia_dominante'] = 0
        caracteristicas['potencia_espectral'] = 0
        caracteristicas['entropia_espectral'] = 0

    # Características de wavelet
    try:
        coeffs = pywt.wavedec(senal, 'db4', level=4)
        for i, coeff in enumerate(coeffs):
            caracteristicas[f'energia_wavelet_nivel_{i}'] = np.sum(np.square(coeff))
    except Exception as e:
        for i in range(5):
            caracteristicas[f'energia_wavelet_nivel_{i}'] = 0

    # Características de dominio del tiempo (HRV)
    try:
        # Detectar picos R
        peaks, _ = signal.find_peaks(senal, distance=int(fs*0.6), height=np.max(senal)*0.3)

        if len(peaks) > 1:
            rr_intervals = np.diff(peaks) / fs * 1000  # Convertir a ms

            caracteristicas['rr_mean'] = np.mean(rr_intervals)
            caracteristicas['rr_std'] = np.std(rr_intervals)
            caracteristicas['rr_min'] = np.min(rr_intervals)
            caracteristicas['rr_max'] = np.max(rr_intervals)

            # RMSSD - Raíz cuadrada de la media de diferencias al cuadrado
            if len(rr_intervals) > 1:
                caracteristicas['hrv_rmssd'] = np.sqrt(np.mean(np.diff(rr_intervals)**2))
            else:
                caracteristicas['hrv_rmssd'] = 0

            # pNN50 - Porcentaje de intervalos RR que difieren más de 50ms
            diff_rr = np.abs(np.diff(rr_intervals))
            caracteristicas['pnn50'] = np.sum(diff_rr > 50) / len(diff_rr) * 100
        else:
            # Si no se detectan picos suficientes
            for key in ['rr_mean', 'rr_std', 'rr_min', 'rr_max', 'hrv_rmssd', 'pnn50']:
                caracteristicas[key] = 0
    except Exception as e:
        for key in ['rr_mean', 'rr_std', 'rr_min', 'rr_max', 'hrv_rmssd', 'pnn50']:
            caracteristicas[key] = 0

    return caracteristicas

"""# Extracción de latidos"""

def extraer_latidos(senal, anotaciones, fs=360, ventana=180):
    # Extracción de ventanas de latidos individuales
    latidos = []
    etiquetas = []

    # Si hay anotaciones (base MIT-BIH)
    if len(anotaciones.symbol) > 0:
        for i, muestra in enumerate(anotaciones.sample):
            inicio = muestra - ventana
            fin = muestra + ventana

            # Validación de índices
            if inicio >= 0 and fin < len(senal):
                latido = senal[inicio:fin]
                if len(latido) == ventana * 2:  # Solo latidos completos
                    latidos.append(latido)
                    etiquetas.append(anotaciones.symbol[i])
    else:
        # Sin anotaciones: detectar picos automáticamente (para CSV)
        senal_norm = (senal - np.mean(senal)) / np.std(senal)
        threshold = np.mean(senal_norm) + 1.5 * np.std(senal_norm)
        peaks, _ = signal.find_peaks(senal_norm, height=threshold, distance=int(fs * 0.3))
        
        for peak in peaks:
            inicio = peak - ventana
            fin = peak + ventana
            
            if inicio >= 0 and fin < len(senal):
                latido = senal[inicio:fin]
                if len(latido) == ventana * 2:
                    latidos.append(latido)
                    etiquetas.append('N')  # Etiquetar como normal por defecto

    return np.array(latidos), etiquetas

