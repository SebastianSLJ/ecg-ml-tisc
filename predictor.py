def clasificar_nuevo_latido(senal, modelo, scaler, fs=360):
    """Clasifica un nuevo latido ECG"""
    senal_preprocesada = preprocesar_ecg(senal, fs)
    caracteristicas = extraer_caracteristicas_avanzadas(senal_preprocesada, fs)

    X_nuevo = np.array(list(caracteristicas.values())).reshape(1, -1)
    X_nuevo_esc = scaler.transform(X_nuevo)

    prediccion = modelo.predict(X_nuevo_esc)
    probabilidades = modelo.predict_proba(X_nuevo_esc)

    return prediccion[0], probabilidades[0]

